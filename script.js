// Enhanced language functions
function setLanguage(lang) {
  if (isLoading) return

  isLoading = true
  showLoadingScreen()

  // Update loading screen language info
  updateLoadingLanguageInfo(lang)

  // Simulate realistic loading with progress
  simulateLoadingProgress(() => {
    currentLanguage = lang
    localStorage.setItem("kodakasoft-language", lang)

    updateContent()
    updateDocumentLanguage()
    updatePageTitle()
    updateLanguageSwitcher()

    hideLanguageSelector()
    hideLoadingScreen()
    isLoading = false
  })
}

function updateLoadingLanguageInfo(lang) {
  const langData = languageData[lang]
  if (loadingLangDisplay && langData) {
    loadingLangDisplay.textContent = langData.native
  }
}

function simulateLoadingProgress(callback) {
  const progressFill = document.querySelector(".loading-progress-fill")
  const progressPercentage = document.querySelector(".loading-percentage")

  let progress = 0
  const interval = setInterval(() => {
    progress += Math.random() * 15
    if (progress > 100) progress = 100

    if (progressFill) progressFill.style.width = progress + "%"
    if (progressPercentage) progressPercentage.textContent = Math.round(progress) + "%"

    if (progress >= 100) {
      clearInterval(interval)
      setTimeout(callback, 300)
    }
  }, 100)
}

function updateLanguageSwitcher() {
  const langData = languageData[currentLanguage]
  if (!langData) return

  // Update header language switcher
  if (currentLangFlag) currentLangFlag.textContent = langData.flag
  if (currentLangCode) currentLangCode.textContent = langData.code

  // Update mobile language select
  if (mobileLanguageSelect) {
    mobileLanguageSelect.value = currentLanguage
  }

  // Update dropdown active state
  document.querySelectorAll(".language-dropdown-item").forEach((item) => {
    const lang = item.getAttribute("data-lang")
    if (lang === currentLanguage) {
      item.classList.add("active")
    } else {
      item.classList.remove("active")
    }
  })
}

function initializeLanguageSwitcher() {
  // Header language switcher
  if (languageSwitcherBtn) {
    languageSwitcherBtn.addEventListener("click", (e) => {
      e.stopPropagation()
      languageDropdown.classList.toggle("active")
      document.querySelector(".language-switcher").classList.toggle("active")
    })
  }

  // Close dropdown when clicking outside
  document.addEventListener("click", (e) => {
    if (!e.target.closest(".language-switcher")) {
      languageDropdown.classList.remove("active")
      document.querySelector(".language-switcher").classList.remove("active")
    }
  })

  // Dropdown language options
  document.querySelectorAll(".language-dropdown-item").forEach((item) => {
    item.addEventListener("click", () => {
      const lang = item.getAttribute("data-lang")
      setLanguage(lang)
      languageDropdown.classList.remove("active")
      document.querySelector(".language-switcher").classList.remove("active")
    })
  })

  // Mobile language select
  if (mobileLanguageSelect) {
    mobileLanguageSelect.addEventListener("change", (e) => {
      const lang = e.target.value
      setLanguage(lang)
    })
  }
}

